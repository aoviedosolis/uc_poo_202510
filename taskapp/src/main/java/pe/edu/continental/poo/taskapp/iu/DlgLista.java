    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String descripcion = txtDescripcion.getText();
        
        if(nombre.isEmpty() || descripcion.isEmpty()){
            JOptionPane.showMessageDialog(this, "Rellene todos los campos en blanco", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            ListaTareasJpaController ltjc = new ListaTareasJpaController(EntityManagerSingleton.getInstance().getEntityManagerFactory());
            ListaTareas lista = new ListaTareas();
            lista.setNombre(nombre);
            lista.setDescripcion(descripcion);
            lista.setUsuario(usuario);
            
            ltjc.create(lista);
            JOptionPane.showMessageDialog(this, "Lista de tareas creada", "Lista de tareas", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }
        
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed


